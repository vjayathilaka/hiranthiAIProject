    private boolean validateDistrictZScore() {
        for (int i = 0; i < districtPanel.getComponentCount(); i++) {
            JComponent child = (JComponent) districtPanel.getComponent(i);
            if (child instanceof JTextField) {
                System.out.println("DEBUG: JTextField: " + i);
                if (((JTextField) child).getText().equals("")) {
                    return false;
                }
            }
        }

        return true;
    }

