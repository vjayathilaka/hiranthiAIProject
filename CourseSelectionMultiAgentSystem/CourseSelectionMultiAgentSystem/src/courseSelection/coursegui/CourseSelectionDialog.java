    private void lst_offeredUniversitiesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lst_offeredUniversitiesMouseClicked
        JList<OfferedUniversity> offeredUniversityList = (JList<OfferedUniversity>) evt.getSource();

        // Get index of item clicked
        int index = offeredUniversityList.locationToIndex(evt.getPoint());
        OfferedUniversity offeredUniversity = (OfferedUniversity) offeredUniversityList.getModel().getElementAt(index);

        // Toggle selected state
        offeredUniversity.setIsSelected(!offeredUniversity.isSelected());

        // Repaint cell
        offeredUniversityList.repaint(offeredUniversityList.getCellBounds(index, index));

        if (offeredUniversity.isSelected()) {
            selectedUniversityList.add(offeredUniversity);
        } else {
            selectedUniversityList.remove(offeredUniversity);
        }
        System.out.println("DEBUG: University: " + offeredUniversity.toString() + " isSelected: " + offeredUniversity.isSelected());
        System.out.println("DEBUG: selectedUniversityList: " + selectedUniversityList);
    }//GEN-LAST:event_lst_offeredUniversitiesMouseClicked

